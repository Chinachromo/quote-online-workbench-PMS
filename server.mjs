function mergeProducts(existing, incoming) {
  const byCode = new Map();
  for (const product of existing) {
    if (product?.code) byCode.set(normalizeCode(product.code), product);
  }
  for (const product of incoming) {
    if (product?.code) byCode.set(normalizeCode(product.code), product);
  }
  return [...byCode.values()];
}

