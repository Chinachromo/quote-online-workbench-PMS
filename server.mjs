function productSearchScore(product, rawQuery) {
  const q = normalizeHeader(rawQuery);
  if (!q) return 0;
  const code = normalizeHeader(product.code);
  const name = normalizeHeader(product.name);
  const desc = normalizeHeader(product.description);
  const all = searchText(product);
  if (code === q) return 100;
  if (code.startsWith(q)) return 80;
  if (code.includes(q)) return 70;
  if (name.includes(q)) return 60;
  if (desc.includes(q)) return 55;
  const tokens = q.split(/[\s+]+/).filter(Boolean);
  if (tokens.length && tokens.every((token) => all.includes(token))) return 40;
  return all.includes(q) ? 30 : 0;
}

function parseMultipart(buffer, contentType) {
  const boundary = contentType.match(/boundary=(?:"([^"]+)"|([^;]+))/)?.[1] || contentType.match(/boundary=(?:"([^"]+)"|([^;]+))/)?.[2];
  if (!boundary) throw new Error("没有找到上传边界");
  const delimiter = Buffer.from(`--${boundary}`);
  const parts = [];
  let start = buffer.indexOf(delimiter);
  while (start >= 0) {
    const next = buffer.indexOf(delimiter, start + delimiter.length);
    if (next < 0) break;
    let part = buffer.slice(start + delimiter.length, next);
    if (part.slice(0, 2).toString() === "\r\n") part = part.slice(2);
    if (part.slice(-2).toString() === "\r\n") part = part.slice(0, -2);
    const headerEnd = part.indexOf(Buffer.from("\r\n\r\n"));
    if (headerEnd > 0) {
      const headerText = part.slice(0, headerEnd).toString("latin1");
      const body = part.slice(headerEnd + 4);
      const name = headerText.match(/name="([^"]+)"/)?.[1] || "";
      const filename = headerText.match(/filename="([^"]*)"/)?.[1] || "";
      parts.push({ name, filename, body });
    }
    start = next;
  }
  return parts;
}

