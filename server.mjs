async function readBody(req, limit = 15 * 1024 * 1024) {
  const chunks = [];
  let size = 0;
  for await (const chunk of req) {
    size += chunk.length;
    if (size > limit) throw new Error("上传文件太大");
    chunks.push(chunk);
  }
  return Buffer.concat(chunks);
}

