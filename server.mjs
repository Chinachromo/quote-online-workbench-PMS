function productsFromRows(rows) {
  if (!rows.length) return [];
  let headerRowIndex = 0;
  let bestScore = 0;
  for (let i = 0; i < Math.min(rows.length, 20); i++) {
    const score = headerScore(rows[i]);
    if (score > bestScore) {
      bestScore = score;
      headerRowIndex = i;
    }
  }
  const headers = rows[headerRowIndex];
  const headerMap = new Map(headers.map((header, index) => [normalizeHeader(header), index]));
  const products = [];

  for (const row of rows.slice(headerRowIndex + 1)) {
    const code = pick(row, headerMap, PRODUCT_HEADER_ALIASES.code);
    if (!code) continue;
    const dealerPrice = numberOrBlank(pick(row, headerMap, PRODUCT_HEADER_ALIASES.dealerPrice));
    const suggestedPrice = numberOrBlank(pick(row, headerMap, PRODUCT_HEADER_ALIASES.suggestedPrice));
    const temporaryDealerPrice = numberOrBlank(pick(row, headerMap, PRODUCT_HEADER_ALIASES.temporaryDealerPrice));
    const name = pick(row, headerMap, PRODUCT_HEADER_ALIASES.name);
    const english = pick(row, headerMap, PRODUCT_HEADER_ALIASES.english);
    products.push({
      category: pick(row, headerMap, PRODUCT_HEADER_ALIASES.category),
      name,
      description: name || english || String(code).trim(),
      english,
      brand: pick(row, headerMap, PRODUCT_HEADER_ALIASES.brand),
      code: String(code).trim(),
      unit: pick(row, headerMap, PRODUCT_HEADER_ALIASES.unit),
      dealerPrice,
      temporaryDealerPrice: temporaryDealerPrice || null,
      suggestedPrice,
      pack: pick(row, headerMap, PRODUCT_HEADER_ALIASES.pack),
      image: pick(row, headerMap, PRODUCT_HEADER_ALIASES.image),
      moq: pick(row, headerMap, PRODUCT_HEADER_ALIASES.moq),
      recommendedQty: pick(row, headerMap, PRODUCT_HEADER_ALIASES.recommendedQty),
    });
  }

  return products;
}

function mergeProducts(existing, incoming) {
  const byCode = new Map();
  for (const product of existing) {
    if (product?.code) byCode.set(normalizeCode(product.code), product);
  }
  for (const product of incoming) {
    if (product?.code) byCode.set(normalizeCode(product.code), product);
  }
  return [...byCode.values()];
}

